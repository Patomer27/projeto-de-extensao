// Variável do tipo String (armazena textos)
let nome = "Pedro";

// Variável do tipo int (armazena números inteiros)
let idade = 20;

// Variável do tipo double (armazena números decimais)
let altura = 1.78;

// Variável do tipo char (armazena apenas um caractere)
let sexo = 'M';

// Variável do tipo boolean (armazena verdadeiro ou falso)
let estudante = true;

// Exibindo os valores das variáveis
console.log("Nome: " + nome);
console.log("Idade: " + idade);
console.log("Altura: " + altura);
console.log("Sexo: " + sexo);
console.log("Estudante: " + estudante);