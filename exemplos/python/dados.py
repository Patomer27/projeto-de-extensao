# Variável do tipo String (armazena textos)
nome = "Pedro"

# Variável do tipo int (armazena números inteiros)
idade = 20

# Variável do tipo double (float) -> armazena números decimais
altura = 1.78

# Variável do tipo char -> armazena apenas um caractere
sexo = 'M'

# Variável do tipo boolean -> armazena verdadeiro ou falso
estudante = True

# Exibindo os valores das variáveis
print("Nome: " + nome)
print("Idade: " + str(idade))
print("Altura: " + str(altura))
print("Sexo: " + sexo)
print("Estudante: " + str(estudante))