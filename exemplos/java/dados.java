public class dados {
    public static void main(String[] args) {

        // Variável do tipo String (armazena textos)
        String nome = "Pedro";

        // Variável do tipo int (armazena números inteiros)
        int idade = 20;

        // Variável do tipo double (armazena números decimais)
        double altura = 1.78;

        // Variável do tipo char (armazena apenas um caractere)
        char sexo = 'M';

        // Variável do tipo boolean (armazena verdadeiro ou falso)
        boolean estudante = true;

        // Exibindo os valores das variáveis
        System.out.println("Nome: " + nome);
        System.out.println("Idade: " + idade);
        System.out.println("Altura: " + altura);
        System.out.println("Sexo: " + sexo);
        System.out.println("Estudante: " + estudante);
    }
}
